def build_script_repair_user_prompt(
    query: str,
    script: str,
    fact_cards: list[dict[str, Any]],
    unsupported: list[dict[str, Any]],
) -> str:
    return f"""\
## Запрос пользователя:
{query}


## Ниже исходный сценарий подкаста:
{script}


## Ниже список неподтверждённых утверждений, найденных при фактологической проверке:
{json.dumps(unsupported, ensure_ascii=False)}


## Ниже список доступных фактов:
{json.dumps(fact_cards, ensure_ascii=False)}
"""


def build_fact_cards_batch_user_prompt(blocks: list[dict[str, Any]], query: str) -> str:
    print("BLOCKS\n -----------------------------------\n", blocks, "BLOCKS\n -----------------------------------\n")
    return f"""
## Запрос пользователя:


{query}


## БЛОКИ (каждый блок — отдельная статья, используй факты только из её контекста):


{json.dumps(blocks, ensure_ascii=False)}
"""


def build_fact_check_user_prompt(facts_flat: list[dict[str, Any]], script: str, request: str) -> str:
    print("facts_flat\n -----------------------------------\n", facts_flat, "facts_flat\n -----------------------------------\n")
    return f"""
## Запрос пользователя:


{request}


## Список фактов:


{json.dumps(facts_flat, ensure_ascii=False)}


## Составленный сценарий:


{script}
"""


def build_outline_and_script_user_prompt(
    query: str,
    compact: list[dict[str, Any]],
    facts_flat: list[dict[str, Any]],
) -> str:

    return f"""
## Запрос пользователя:


{query}


## Список фактов для ссылок и использования:


{json.dumps(facts_flat, ensure_ascii=False)}
"""


def build_outline_and_script_user_prompt_strict_refs(
    query: str,
    compact: list[dict[str, Any]],
    facts_flat: list[dict[str, Any]],
    known_fact_ids: Iterable[str],
) -> str:
    base = build_outline_and_script_user_prompt(
        query=query,
        compact=compact,
        facts_flat=facts_flat,
    )
    known = list(known_fact_ids)

    return (
        base
        + "\n\nСТРОГОЕ ПРАВИЛО:\n"
          "Используй ТОЛЬКО эти fact_id в ссылках вида [A#-F#]. Никаких других:\n"
        + ", ".join(known)
    )